/*Estrutura de repetição WHILE*/
var contador = 1
while (contador <= 10){
    console.log(`${contador}`)
    contador++
}

/*Estrutura de repetição DO*/
var contar = 1
do{
    console.log(`${contar}`)
    contar++
} while(contar <=10)


/*Estrutura de repetição FOR*/
for(var conte=1; conte<=10;conte++){
    console.log(`${conte}`)
}